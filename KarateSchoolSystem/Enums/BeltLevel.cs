namespace KarateSchoolSystem.Enums;

/// <summary>
/// Represents the belt rank of a student, ordered lowest to highest so
/// promotion logic can verify advancement by exactly one rank.
/// </summary>
public enum BeltLevel
{
    White = 0,
    Yellow = 1,
    Orange = 2,
    Green = 3,
    Blue = 4,
    Purple = 5,
    Brown = 6,
    Black = 7
}
